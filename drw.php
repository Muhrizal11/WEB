            <h1 class="mb-10 text-center" >Sekilas Tentang Wisata Duren Sari</h2>
            	<p style="text-align: justify;">>Desa Wisata Duren Sari Sawahan merupakan desa wisata yang berdiri sejak tanggal 11 November 2015 diresmikan oleh Bappeda Provinsi Jawa Timur. Desa Wisata Duren Sari bersekretariat di RT 12 RW 04 Dusun Singgahan Desa Sawahan Kecamatan Watulimo Kabupaten Trenggalek. Desa Wisata Duren Sari Sawahan dikelola oleh Kelompok Sadar Wisata (Pokdarwis) Duren Sari dibawah bimbingan Dinas Pariwisata dan Kebudayaan Kabupaten Trenggalek, Bappeda Kabupaten Trenggalek, Bappeda Provinsi Jawa Timur, Kepala Desa Sawahan.</p>
              <p style="text-align: justify;">Desa Wisata Duren Sari Sawahan menyuguhkan paket wisata alam,permainan tradisional, outbond, river tubing,  kuliner serta homestay bagi para wisatawan. Sudah banyak pengunjung yang menikmati paket wisata di Desa Wisata Sawahan antara lain masyarakat luar kota dan beberapa turis dari Amerika, Florida, Canada. Paket yang disuguhkan mulai dari paket satu hari hingga menginap 2 hari 2 malam.</p>
              <p style="text-align: justify;">Memasuki Desa Wisata Duren Sari Sawahan, wisatawan disuguhi welcome drink wedang jeser. Wedang Jeser khas ini hanya ada di Desa Wisata Duren Sari Sawahan disajikan menggunakan teko dan cangkir bumbung yang terbuat dari bambu. Teko dan cangkir bumbung ini merupakan hasil produksi dari masyarakat sekitar. Wedang Jeser hangat sangat pas disajikan dengan suasana desa.</p>
              <p style="text-align: justify;">Paket Wisata Alam terdiri atas paket jelajah alas duren IDF (International Durio Foresty), yaitu hutan durian terluas di Asia Tenggara. Hutan Durian IDF ini diresmikan oleh Menteri Pertanian pada tanggal 13 Mei 2016. Wisatawan akan diajak jelajah alas duren, diperkenalkan dengan pohon durian yang sudah berumur ratusan tahun, serta diajak merasakan kesegaran kali Bawok. Kali Bawok merupakan kali atau sungai di tengah-tengah alas durian IDF. Wisatawan bisa mandi dan membasuh wajah di kali ini. Wudhu di kali ini dipercaya dapat membuat awet muda. Selain itu wisatawan disuguhi degan atau kelapa muda di tengah alas durian. Wisatawan bisa menikmati kelapa muda di pinggir kali Bawok sebagai pelepas dahaga setelah lelah berjelajah.</p>
              <p style="text-align: justify;">Paket wisata lainnya ialah river tubing. River tubing ini berlokasi di kali Watulawang. Wisatawan bisa menikmati river tubing dengan alat dan perlengkapan yang sesuai standart. Mulai dari helm, baju pelampung, deker tangan dan kaki. Watulawang River Tubing dipandu oleh guide profesional dan dijaga setiap posnya. Wisatawan akan menelusuri kali Watulawang menggunakan ban yang sudah dilengkapi dengan tali keamanan. Sepanjang perjalanan wisatawan akan menikmati pemandangan alas duren. Jarak lintasan ini 700 meter cukup memuaskan dan mengacu adrenalin para wisatawan. Dari lokasi finish menuju lokasi awal disediakan ojek langsung dari anggota Pokdarwis.</p>
              <p style="text-align: justify;"> Paket kuliner terdiri atas makanan khas desa. Sarapan dengan sompil, makanan yang terbuat dari nasi lontong dan sayur buah nangka dengan bumbu rempah. Kemudian siang hari dilanjutkan dengan makan siang khas desa yang terdiri atas nasi thiwul, nasi putih, sayur pakis, sayur daun singkong, gembrot, bothok, ikan goreng, dan sambal ijo. Untuk menu makan malamnya ialah genduri. Yaitu lodho, nasi gurih dan urap. Selain menu sarapan, makan siang dan makan malam juga disajikan snack khas desa antara lain kacang godhog atau kacang rebus, singkong rebus, gethuk, mendhut, pisang rebus, dan lain-lain.</p>
